  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  category_value text NOT NULL,
  category_label text NOT NULL,
  equipment_name text NOT NULL,
  brand text,
  type text,
  created_at timestamptz NOT NULL DEFAULT now()

CREATE POLICY "Anyone can update equipment_catalog" ON public.equipment_catalog FOR UPDATE TO public USING (true);
CREATE POLICY "Anyone can delete equipment_catalog" ON public.equipment_catalog FOR DELETE TO public USING (true);

